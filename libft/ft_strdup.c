#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		srclen;
	char	*dest;
	int		i;

	i = 0;
	srclen = ft_strlen(src);
	dest = malloc(sizeof(char) * srclen + 1);
	if (!dest)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
